<?php

namespace app\admin\controller\unitymanage;

use app\common\controller\Backend;
use app\admin\model\unitymanage\Dynamics;
use think\Db;

/**
 * 动态评论
 *
 * @icon fa fa-circle-o
 */
class Dynamiccomments extends Backend
{
    
    /**
     * Dynamiccomments模型对象
     * @var \app\admin\model\unitymanage\Dynamiccomments
     */
    protected $model = null;
    protected $relationSearch = true;


    public function _initialize()
    {
        parent::_initialize();
        $this->model = new \app\admin\model\unitymanage\Dynamiccomments;

    }
    
    /**
     * 默认生成的控制器所继承的父类中有index/add/edit/del/multi五个基础方法、destroy/restore/recyclebin三个回收站方法
     * 因此在当前控制器中可不用编写增删改查的代码,除非需要自己控制这部分逻辑
     * 需要将application/admin/library/traits/Backend.php中对应的方法复制到当前控制器,然后进行修改
     */
    /**
     * 查看
     */
    public function index()
    {
        //设置过滤方法
        $this->request->filter(['strip_tags']);
        if ($this->request->isAjax()) {
            //如果发送的来源是Selectpage，则转发到Selectpage
            if ($this->request->request('keyField')) {
                return $this->selectpage();
            }
            list($where, $sort, $order, $offset, $limit) = $this->buildparams();

            $with = ['users','users2','dynamics','dynamics2'];

            $total = $this->model
                ->with($with)
                ->where($where)
                ->order($sort, $order)
                ->count();

            $list = $this->model
                ->with($with)
                ->where($where)
                ->order($sort, $order)
                ->limit($offset, $limit)
                ->select();
            //echo $this->model->getlastsql();die;
            foreach ($list as $k => &$v) {
                $v['dynamics']['content'] = urldecode($v['dynamics']['content']);
                if(mb_strlen($v['dynamics']['content'], 'utf8') > 30) {
                    $v['dynamics']['content'] = mb_substr($v['dynamics']['content'], 0, 30, 'utf8').'...';
                }
                $v['dynamics2']['content'] = urldecode($v['dynamics2']['content']);
                if(mb_strlen($v['dynamics2']['content'], 'utf8') > 30) {
                    $v['dynamics2']['content'] = mb_substr($v['dynamics2']['content'], 0, 30, 'utf8').'...';
                }
                 //lzw
                $v['content']=urldecode($v['content']);
                if(mb_strlen($v['content'], 'utf8') > 30) {
                    $v['content'] = mb_substr($v['content'], 0, 30, 'utf8').'...';
                }
            }
            $list = collection($list)->toArray();
            $result = array("total" => $total, "rows" => $list);

            return json($result);
        }
        return $this->view->fetch();
    }

    /**
     * 详情
     */
    public function detail($ids)
    {
        $row = $this->model->get(['id' => $ids]);
        if (!$row)
            $this->error(__('No Results were found'));
        $this->view->assign("row", $row->toArray());
        return $this->view->fetch();
    }

    /**
     * 添加
     */
    public function edits($ids = null)
    {
        $row = Dynamics::get($ids);

        if (!$row) {
            $this->error(__('No Results were found'));
        }
        $adminIds = $this->getDataLimitAdminIds();
        if (is_array($adminIds)) {
            if (!in_array($row[$this->dataLimitField], $adminIds)) {
                $this->error(__('You have no permission'));
            }
        }
        if ($this->request->isPost()) {
            $params = $this->request->post("row/a");
            //print_r($params);die();
            if ($params) {
                $params = $this->preExcludeFields($params);
                $result = false;
                Db::startTrans();
                try {
                    //是否采用模型验证
                    if ($this->modelValidate) {
                        $name = str_replace("\\model\\", "\\validate\\", get_class($this->model));
                        $validate = is_bool($this->modelValidate) ? ($this->modelSceneValidate ? $name . '.edit' : $name) : $this->modelValidate;
                        $row->validateFailException(true)->validate($validate);
                    }
                    $result = $this->model->allowField(true)->save($params);
                    Db::commit();
                } catch (ValidateException $e) {
                    Db::rollback();
                    $this->error($e->getMessage());
                } catch (PDOException $e) {
                    Db::rollback();
                    $this->error($e->getMessage());
                } catch (Exception $e) {
                    Db::rollback();
                    $this->error($e->getMessage());
                }
                if ($result !== false) {
                    $this->success();
                } else {
                    $this->error(__('No rows were updated'));
                }
            }
            $this->error(__('Parameter %s can not be empty', ''));
        }
        //print_r($row);die;
        $this->view->assign("row", $row);
        return $this->view->fetch();
    }

